const int MOD = 1e9+7;

template<typename T> 
inline T gcD(T a,T b){
    if(a < b)swap(a,b);
    while(b){  a = a % b;b ^= a;a ^= b;b ^= a; };
    return a;
}

template<typename T>
inline T pow_mod(T a, T b) {
    T res = 1;
    while(b) {
        if(b & 1)
            res = (res * a) % MOD;
        a = (a * a) % MOD;
        b >>= 1;
    }
    return res;
}

const int MAXN = 1e6+10;
int ans[MAXN<<2];
int o[MAXN<<2];
int c[MAXN<<2];
char str[MAXN];
int N,M;
int qa[MAXN<<2],qo[MAXN<<2],qc[MAXN<<2];

void build_tree(int cur, int s, int e){
    if(s==e){
        if(str[s]==')'){
            c[cur] = 1;
            o[cur] = 0;
        }else{
            o[cur] = 1;
            c[cur] = 0;
        }
        ans[cur] = 0;
    }else{
        int mid = (s+e)>>1;
        int c1 = cur<<1,c2 = c1 | 1;
        build_tree(c1, s, mid);
        build_tree(c2, mid+1, e);
        int t = miN(o[c1], c[c2]);
        ans[cur] = ans[c1]+ans[c2]+t;
        o[cur] = o[c1] + o[c2] - t;
        c[cur] = c[c1] + c[c2] - t;
    }
}

void query(int cur, int s, int e, int St, int En){
    if(En < s || e < St)qa[cur] = qc[cur] = qo[cur] = 0;
    else if(St <= s && e <= En){
        qa[cur] = ans[cur];
        qc[cur] = c[cur];
        qo[cur] = o[cur];
    }else{
        int mid = (s+e)>>1;
        int c1 = cur<<1, c2 = c1 | 1;
        query(c1, s, mid, St, En);
        query(c2, mid+1, e, St, En);
        int t = miN(qo[c1], qc[c2]);
        qa[cur] = qa[c1] + qa[c2] + t;
        qo[cur] = qo[c1] + qo[c2] - t;
        qc[cur] = qc[c1] + qc[c2] - t;
    }
}

int main(){
    ss(str);
    N = strlen(str);
    s(M);
    build_tree(1,0,N-1);
    while(M--){
        int l,r;
        s(l);s(r);
        l--;r--;
        query(1, 0, N-1, l, r);
        printf("%d\n",2*qa[1]);
    }
    return 0;  
}
