ll memo[MAXN];
int ans[MAXN];
int _par[MAXN];

ll powM(ll a, ll b) {
    ll x = 1, y = a;
    while(b > 0) {
        if(b%2 == 1) {
            x=(x*y);
            if(x>MOD) x%=MOD;
        }
        y = (y*y);
        if(y>MOD) y%=MOD;
        b /= 2;
    }
    return x;
}


int DP(int cur,int par){
    _par[cur] = par;
    if(g[cur].size() == 1 && cur != 1)memo[cur] = 1;
    else{
        memo[cur] = 1;
        foreach(v, g[cur]){
            if(*v != par){
                memo[cur] *= (DP(*v, cur)+1);
                memo[cur] %= MOD;
            }
        }
    }
    return memo[cur];
}
void solve(int cur, int par){
    if(cur == 1)ans[cur] = memo[cur];
    else{
        ll as = (ans[par] + memo[cur] + 1);
        as %= MOD;
        as *= memo[cur];
        as %= MOD;
        as *= powM(memo[cur] + 1, MOD-2);
        as %= MOD;
        ans[cur] = as;
    }
    foreach(v, g[cur]){
        if(*v != par){
            solve(*v, cur);
        }
    }
}
